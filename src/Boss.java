public class Boss {
    private int bossHealts;
    private int bossDamage;
    private String typeOfDefence;

    public int getBossHealts() {
        return bossHealts;
    }

    public void setBossHealts(int bossHealts) {
        this.bossHealts = bossHealts;
    }

    public int getBossDamage() {
        return bossDamage;
    }

    public void setBossDamage(int bossDamage) {
        this.bossDamage = bossDamage;
    }

    public String getTypeOfDefence() {
        return typeOfDefence;
    }

    public void setTypeOfDefence(String typeOfDefence) {
        this.typeOfDefence = typeOfDefence;
    }
}
